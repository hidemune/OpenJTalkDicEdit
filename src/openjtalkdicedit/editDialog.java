    public void setStr(String str) {
        if (str.trim().equals("")) {
            text1.setText("");
            text6.setText("");
            text7.setText("");
            text8.setText("");
            return;
        }
        
        String[] field = str.split(",", 15);
        text1.setText(field[0]);
        text2.setText(field[1]);
        text3.setText(field[2]);
        text4.setText(field[3]);
        String cmb = field[4] + "," + field[5] + "," + field[6] + "," + field[7] + "," + field[8] + "," + field[9];
        combo5.addItem(cmb);
        combo5.setSelectedIndex(combo5.getItemCount()-1);
        text6.setText(field[10]);
        text7.setText(field[11]);
        text8.setText(field[12]);
        text9.setText(field[13]);
        text10.setText(field[14]);
    }
